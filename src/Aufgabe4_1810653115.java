public class Aufgabe4_1810653115
{
    public static void main(String[] args)
    {
        //For Schleife
        for(int h=1; h<11; h++)
        {
            System.out.println("Der Schleifenzähler ist: " + h);
        }

        //While Schleife
        int i = 1;
        while(i < 11)
        {
            System.out.println("Der Schleifenzähler ist: " + i);
            i++;
        }

        int j = 1;
        do
        {
            System.out.println("Der Schleifenzähler ist: " + j);
            j++;
        } while(j < 11);

    }

}
